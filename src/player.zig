const std = @import("std");

//-----------------------------------------------------------------------------//
//   Processing
//-----------------------------------------------------------------------------//

pub fn move(x: f32, y: f32) void {
    pos_x += x;
    pos_y += y;
    std.log.debug("Pos: ({d:.1}, {d:.1})", .{pos_x, pos_y});
}

pub fn moveX(x: f32) void {
    pos_x += x;
    std.log.debug("Pos: ({d:.1}, {d:.1})", .{pos_x, pos_y});
}

pub fn moveY(y: f32) void {
    pos_y += y;
    std.log.debug("Pos: ({d:.1}, {d:.1})", .{pos_x, pos_y});
}

//-----------------------------------------------------------------------------//
//   Internal
//-----------------------------------------------------------------------------//

var pos_x: f32 = 10.0;
var pos_y: f32 = 10.0;
var dir: f32 = 0.0;
